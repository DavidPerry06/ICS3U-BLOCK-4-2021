package week1;

public class ExampleThree {
    public static void main(String[] args) {
      //  System.out.println("This is "very" important!"); //double quotes encapsulate string literals - putting a quote in a string literal gets java
    
      System.out.println("This is \"very\" important!"); // the backslash \ escapes a character - creates an escape sequence
    
      //System.out.println("This is \very important!"); // \v is not a valid escape sequence
    
      System.out.println("This is \\very important!"); // you escape a \ with another \
      System.out.println("This\nis\nvery\nimportant!"); // \n is new line

      /**
       * AP exam only tests you on \" \n and \\
       */
    
    }
}